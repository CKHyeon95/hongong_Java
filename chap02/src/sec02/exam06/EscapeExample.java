package sec02.exam06;

public class EscapeExample {

	public static void main(String[] args) {
		
		/*  
		   이스케이프 문자
		 \t      tab만큼 문자 띄어쓰기 
		 \n      줄 바꿈
		 \r      캐리지리턴
		 \"      "출력
		 \'      '출력
		 \\      \출력
		 u16진수  16진수 유니코드에 해당하는 문자 출력
		 
		 */ 
		
		System.out.println("번호\t이름\t직업");
		System.out.println("번호\n이름\n직업");
		
	}

}
